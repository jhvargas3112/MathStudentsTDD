package calculator;

public class IntegerArithmeticCalculator {
  protected int add(int operator1, int operator2) {
    return operator1 + operator2;
  }

  protected int subtract(int operator1, int operator2) {
    return operator1 - operator2;
  }

  protected int multiply(int operator1, int operator2) {
    return operator1 * operator2;
  }

  protected int quotient(int operator1, int operator2) {
    return operator1 / operator2;
  }
}
